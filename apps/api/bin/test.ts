process.env.NODE_ENV = 'test'

/**
 * Tests use a separate database and isolated session/uploads.
 * Values set here take precedence over apps/api/.env (dotenv does not override).
 * See apps/api/.env.test for the full test profile.
 */
process.env.DB_DATABASE = process.env.DB_TEST_DATABASE ?? 'moda_urbana_test'
process.env.SESSION_DRIVER = 'memory'
process.env.STORAGE_LOCAL_PATH = './storage/uploads-test'
process.env.LOG_LEVEL = process.env.LOG_LEVEL ?? 'warn'

if (process.env.DB_DATABASE === 'moda_urbana') {
  throw new Error('Refusing to run tests with DB_DATABASE=moda_urbana. Use moda_urbana_test.')
}

import 'reflect-metadata'
import { Ignitor, prettyPrintError } from '@adonisjs/core'
import { configure, processCLIArgs, run } from '@japa/runner'

/**
 * URL to the application root. AdonisJS need it to resolve
 * paths to file and directories for scaffolding commands
 */
const APP_ROOT = new URL('../', import.meta.url)

/**
 * The importer is used to import files in context of the
 * application.
 */
const IMPORTER = (filePath: string) => {
  if (filePath.startsWith('./') || filePath.startsWith('../')) {
    return import(new URL(filePath, APP_ROOT).href)
  }
  return import(filePath)
}

new Ignitor(APP_ROOT, { importer: IMPORTER })
  .tap((app) => {
    app.booting(async () => {
      await import('#start/env')
    })
    app.listen('SIGTERM', () => app.terminate())
    app.listenIf(app.managedByPm2, 'SIGINT', () => app.terminate())
  })
  .testRunner()
  .configure(async (app) => {
    const { runnerHooks, ...config } = await import('../tests/bootstrap.js')

    processCLIArgs(process.argv.splice(2))
    configure({
      ...app.rcFile.tests,
      ...config,
      ...{
        setup: runnerHooks.setup,
        teardown: runnerHooks.teardown.concat([() => app.terminate()]),
      },
    })
  })
  .run(() => run())
  .catch((error) => {
    process.exitCode = 1
    prettyPrintError(error)
  })
